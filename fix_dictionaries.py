def get_definition(word):
    """Get Traditional Chinese definition for a word."""
    word_lower = word.lower()

    # Direct match
    if word_lower in DEFINITIONS:
        return DEFINITIONS[word_lower]

    # Try without common suffixes for programmatic generation
    base_word = word_lower
    suffix_info = ""

    # Handle common suffixes
    if word_lower.endswith("ing"):
        base = word_lower[:-3]
        if base.endswith("e"):
            base = base
        elif base in DEFINITIONS:
            return f"{DEFINITIONS[base]}（動名詞/進行式）"

    if word_lower.endswith("ed"):
        base = word_lower[:-2]
        if base.endswith("e"):
            base = base[:-1]
        if base in DEFINITIONS:
            return f"{DEFINITIONS[base]}（過去式/過去分詞）"

    if word_lower.endswith("s") and len(word_lower) > 3:
        base = word_lower[:-1]
        if base in DEFINITIONS:
            return f"{DEFINITIONS[base]}（複數/第三人稱單數）"

    if word_lower.endswith("er"):
        base = word_lower[:-2]
        if base in DEFINITIONS:
            return f"{DEFINITIONS[base]}的人；更{DEFINITIONS[base]}"

    if word_lower.endswith("est"):
        base = word_lower[:-3]
        if base in DEFINITIONS:
            return f"最{DEFINITIONS[base]}"

    if word_lower.endswith("ly"):
        base = word_lower[:-2]
        if base in DEFINITIONS:
            return f"{DEFINITIONS[base]}地"

    if word_lower.endswith("ness"):
        base = word_lower[:-4]
        if base in DEFINITIONS:
            return f"{DEFINITIONS[base]}的性質"

    if word_lower.endswith("tion") or word_lower.endswith("sion"):
        # These are usually nouns formed from verbs
        return f"{word}（名詞）"

    if word_lower.endswith("ment"):
        base = word_lower[:-4]
        if base in DEFINITIONS:
            return f"{DEFINITIONS[base]}（名詞形式）"

    # If we can't find it, return a placeholder that needs manual review
    return f"{word}（需要定義）"

def generate_sentence(word, part_of_speech):
    """Generate a natural English sentence using the word."""
    word_lower = word.lower()
    pos_lower = part_of_speech.lower()

    # Verb sentences
    if "動詞" in part_of_speech:
        if word_lower.endswith("ing"):
            return f"She is {word_lower} right now."
        elif word_lower.endswith("ed"):
            return f"They {word_lower} yesterday."
        elif word_lower.endswith("s"):
            return f"He {word_lower} every day."
        else:
            return f"I {word_lower} regularly."

    # Noun sentences
    elif "名詞" in part_of_speech:
        if word_lower.endswith("s"):
            return f"These {word_lower} are important."
        elif word_lower.endswith("tion") or word_lower.endswith("sion"):
            return f"The {word_lower} was successful."
        else:
            return f"The {word_lower} is very interesting."

    # Adjective sentences
    elif "形容詞" in part_of_speech:
        if word_lower.endswith("ed"):
            return f"I felt {word_lower} after the event."
        elif word_lower.endswith("ing"):
            return f"It was a {word_lower} experience."
        else:
            return f"The weather is {word_lower} today."

    # Adverb sentences
    elif "副詞" in part_of_speech:
        return f"She spoke {word_lower} to the audience."

    # Default
    return f"The {word_lower} was mentioned in class."

def fix_entry(entry):
    """Fix a single dictionary entry."""
    word = entry.get("word", "")
    current_def = entry.get("definition", "")
    current_sentence = entry.get("sentence", "")
    part_of_speech = entry.get("partOfSpeech", "名詞")

    # Check if definition needs fixing
    needs_def_fix = (
        "（詞）" in current_def or
        "（動詞）" in current_def and current_def == f"{word}（動詞）" or
        "（名詞）" in current_def and current_def == f"{word}（名詞）" or
        "（形容詞）" in current_def and current_def == f"{word}（形容詞）" or
        current_def == word or
        not current_def or
        current_def.endswith("的中文意思")
    )

    # Check if sentence needs fixing
    needs_sentence_fix = (
        f"The {word} is interesting" in current_sentence or
        f"The {word} was" in current_sentence and "interesting" in current_sentence or
        not current_sentence or
        len(current_sentence.split()) < 4
    )

    # Fix definition
    if needs_def_fix:
        entry["definition"] = get_definition(word)

    # Fix sentence
    if needs_sentence_fix:
        entry["sentence"] = generate_sentence(word, part_of_speech)

    return entry

def fix_file(file_path):
    """Fix all entries in a JSON file."""
    print(f"\nProcessing: {file_path}")

    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    if not isinstance(data, list):
        print(f"Error: Expected list, got {type(data)}")
        return

    total = len(data)
    fixed_count = 0

    for i, entry in enumerate(data):
        if i % 100 == 0:
            print(f"  Progress: {i}/{total} ({i*100//total}%)")

        original_entry = entry.copy()
        fixed_entry = fix_entry(entry)

        if fixed_entry != original_entry:
            fixed_count += 1
            data[i] = fixed_entry

    # Write back to file
    with open(file_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"  Fixed {fixed_count}/{total} entries ({fixed_count*100//total}%)")
    print(f"  ✓ Completed: {file_path}")

