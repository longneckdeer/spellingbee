print()

# Separate into university and expert
university_words = []
still_expert_words = []

for word_data in expert_words:
    word = word_data['word'].lower()

    # Check if it's a common word (both common AND relatively easy to spell)
    if word in COMMON_WORDS:
        # Additional check: word should have reasonable length (≤12 chars typically)
        # and not have overly complex spelling patterns
        if len(word) <= 12:
            # Move to university (difficulty 71-90)
            word_data['difficulty'] = min(90, max(71, word_data.get('difficulty', 85)))
            university_words.append(word_data)
        else:
            # Long common words stay in expert but get lower difficulty
            word_data['difficulty'] = min(95, word_data.get('difficulty', 95))
            still_expert_words.append(word_data)
    else:
        # Not common - stays in expert
        still_expert_words.append(word_data)

print(f"✓ {len(university_words)} words moved to university level")
print(f"✓ {len(still_expert_words)} words remain in expert level")
print()

