// Process dictionary files
function convertDefinitions(filename) {
  const filePath = path.join(__dirname, '..', 'src', 'gameTypes', 'english-spelling', 'data', filename)
  console.log(`\nüìù Processing ${filename}...`)

  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'))
  let converted = 0
  let notFound = 0

  const updated = data.map(entry => {
    if (ENGLISH_DEFINITIONS[entry.word]) {
      converted++
      return {
        ...entry,
        definition: ENGLISH_DEFINITIONS[entry.word]
      }
    } else {
      notFound++
      // Keep existing definition if no English one found
      return entry
    }
  })

  fs.writeFileSync(filePath, JSON.stringify(updated, null, 2), 'utf8')
  console.log(`  ‚úÖ Converted: ${converted}`)
  console.log(`  ‚ö†Ô∏è  Not found: ${notFound}`)
  return { converted, notFound }
}

